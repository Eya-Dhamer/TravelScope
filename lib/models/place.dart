class Place {
  final String name;
  final String country;
  final String imagePath;
  final String description;
  final double rating;
  final double price;
  final int reviews;

  Place({
    required this.name,
    required this.country,
    required this.imagePath,
    required this.description,
    required this.rating,
    required this.price,
    required this.reviews,
  });
}